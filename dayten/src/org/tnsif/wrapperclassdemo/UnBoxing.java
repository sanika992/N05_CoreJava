package org.tnsif.wrapperclassdemo;

 /*program to demonstrate on UnBoxing 
  * Definition: Conversion of object to primitive type
  */
public class UnBoxing {

	public static void main(String[] args) {
		Integer num = 99;
		//converting object type to primitive type
		int x=num;
		System.out.println(x);

	}

}
