package org.tnsif.wrapperclassdemo;
/*program to demonstrate on AutoBoxing
 * Definition: Conversion of Primitive type to wrapper class object type
 */
public class AutoBoxing {

	public static void main(String[] args) {
		
       char c='A';
       //converting primitive type to object type
       Character ch=c;
       System.out.println(ch);
	}

}
